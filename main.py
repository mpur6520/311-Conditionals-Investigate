number1 = int(input("Please enter a number")) #asks the user to enter a number and stores it in the variable number1 as a numerical value
number2 = int(input("Please enter another number")) #asks the user to enter a number and stores it in the variable number2 as a numerical value

if number1 > number2: #checks if number1 is bigger than number2
  print("Number 1 is bigger than number 2") #print that is number1 is bigger than number2
elif number2 > number1: #checks if number2 is bigger than number1
  print("Number 2 is bigger than number 1") #print that number2 is bigger than number1
else: #if both conditionals are false
  print("Both numbers are the same") #print the numbers are the same or equal


